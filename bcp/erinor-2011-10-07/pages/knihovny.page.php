<h1>Knihovny</h1>
<p><strong>Knihovna místodržícího &hellip;</strong></p>
<p>Tato nepříliš velká knihovna obsahuje množství svazků trochu nekvalitní literatury sjednocování, např. Carles Ynek Hacha - Jaro, a pár naučných spisů, které pan místodržící dovoluje za jeho přítomnosti číst.</p>
<p>Z naučných spisů je takto k osvětě přístupno několik svazků pojednávajících o farmaření a sadařství. Déle pak dva o produkčních řemeslech( zvláště hornictví ) a jeden o kovotepectví. K nalezení je zde také pár dalších knih a něco, co bylo kdysi knihou, ale dnes je to zbytek ohořelých a zabarvených papírů zabalených v kusu pergamenu.</p>
<p>Zbytek písemností nedává pan &hellip; nikomu číst. </p>