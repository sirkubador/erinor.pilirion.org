<h1>Modlitba</h1>

<p>Na starém hřbitově se objevilo několik nových hrobů. Mě však zaujaly jen dva. Byly blízko sebe, tak blízko, že se dalo dotknout obou náhrobků současně. I jinak se lišily od ostatních. Na první pohled bylo patrné, že patřily nějakým důležitým osobám, neboť byly o něco honosnější, než okolní...
</p><p>Byl časný podzim, brzy zrána a na pavučině, kterou již stačil pavouk usnovat mezi oběma náhrobky, se třpytily kapky rosy. Na čerstvé hlíně byla položena nádherná kytice. Snad jen zázrakem ještě ten, kdo ji sem položil, našel takové květy. A pod ní kdosi prstem vyryl jediné slovo - PŘÍTEL .
</p><p>Potom mi zrak sklouzl na postavu mladé ženy, která klečela mezi hroby, a jejíž ramena se otřásala vzlyky.
</p><p>Přiletěl jsem blíž a usedl na vedlejší kámen. Když se trochu uklidnila zaslechl jsem mezi několika posledními slzami pár tónů. Napřed nejistých, ale pak jako by všechen její smutek vytryskl právě tou písní, která měla být zároveň modlitbou a posledním rozloučením s přáteli, které již nikdy nespatří.
</p><p>Přidal jsem se k ní, neboť mě dojala hloubka té písně a svým slavičím trylkem jsem doprovodil její poslední sbohem.
</p>
