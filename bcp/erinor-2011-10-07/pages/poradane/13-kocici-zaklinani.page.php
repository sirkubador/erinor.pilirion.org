<h4>Uprchlíci</h4>

<p>Uprchlíci z vedlejší vesnice prožili před krátkou dobou katastrofu, která je stála jejich domovy a mnohé z jejích blízkých i životy. Nyní žijí v nuzných podmínkách ve vyhnanství ve vedlejší vesnici. Přes léto je okolí uživí, ale až přijde zima, kdo ví. Jde o ideální podhoubí pro vyhrocení všech drobných problémů, které dříve zapadly, ale nyní znovu vyplouvají na povrch v plné míře. Podaří se vesničanům své rozepře vyřešit, najít cestu jak z této situace ven, případně zvrátit to, co způsobilo zničení jejich vesnice? Odpovědi na tyto otázky, můžete na Erinoru dát vy.
</p><p>Uprchlíci budou nejpropracovanější skupina z hlediska vztahů a vždy budou mít mnoho problémů k řešení. Na druhou stranu výběr postav bude do jisté míry omezený.
