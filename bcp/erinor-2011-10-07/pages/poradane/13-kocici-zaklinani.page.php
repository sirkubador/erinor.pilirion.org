<h4>Přistěhovalci</h4>

<p>Do tohoto kraje v nedávné době přicestovalo mnoho lidí a začínají zde žít své životy a plnit svoje plány a cíle. Na sever do Nolgitu přichází často ti, kterým se doma nedařilo, měli konflikty se zákonem nebo s někým mocným. Přichází sem vyhnanci a chudáci, ale také dobrodruzi, kterým nechutnal klidný život v civilizovanějších částech Lithénu nebo je zajímá kraj známý z pohádek. Tito lidé mají většinou jen skromný majetek, který si dokázali přinést na zádech a jejich živobytí závisí na jejich schopnosti přizpůsobit se a užitečnosti ostatním. Na druhou stranu přichází do úžasného světa plného dobrodružství.
</p><p>Přistěhovalci budou ve hře nejméně propojení s ostatními postavami, budou mít tedy od nás připraveno jen minimální množství osobních qestů. Za to však mají větší svobodu při tvorbě postavy a osobní cíle (a tedy i svůj qest) si mohou hráči přistěhovalců vymyslet sami. Samozřejmě i zde se najdou výjimky v podobě postav s předepsanou historií a vztahy, ale je jich minimum.
