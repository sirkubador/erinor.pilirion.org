  V tento čas nebudete hrát svoji normální postavu, která bude v této době pro ostatní nedostupná.
   Vaše postava bude zkrátka spát. Druhou roli budete mít u některých postav přesně zadanou a budete mít minimální možnost ji změnit. Ostatní si jí mohou vzít pokud mají zájem.
