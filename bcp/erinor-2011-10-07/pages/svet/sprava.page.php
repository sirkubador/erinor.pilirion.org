<h2>Správa oblasti</h2>
<p>Část Nolgitu, Sedmipahorčí (, kde se odehrává Erinor - Kočičí Zaklínání) leží na pomezí dvou šlechtických panství.
Pokud si to dobře pamatujete, Midsume-Nass leželo v jednom panství a Lochder ve druhém.
</p><p>Ale ono je to vlastně jedno, místní šlechta není moc silná a tyto pozemky jí už dávno přestaly zajímat.
Nepravidelně, jednou za pár let si někdo vzpomene, že by sem měl poslat výběrčího daní, jinak se v oblasti neprojevuje. Požadované daně jsou většinou tak nízké, že není problém je uhradit. A tak si místní obce vládnou samy.
</p><p>Každá ves a městečko má svého starostu. Je to většinou nejschopnější člověk ve vesnici. Ves si ho volí, když je to potřeba, tedy pokud už starý starosta nezvládá svojí práci nebo pokud zemře. Volí se obvykle podle systému jedna chalupa, jeden hlas. Lochder i Midsume-Nass jsou už několik let pod správou svých starostů a zatím jsou všichni spokojení, nikdo se zatím nenažil nic změnit.
</p><p>Městečko má ještě pro svoji ochranu malou domobranu, tedy skupinu mužů, kteří dokáží alespoň trochu ovládat zbraně a jsou ochotni kdykoli chránit svoje sousedy.
</p>
