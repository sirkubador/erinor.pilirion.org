<h3>Ranné novodobí</h3>
<p>Období po vydání dekretu Eirského (627). Je silně poznamenáno společenskými změnami, poklesem kultury i literatury, válkami a čistkami na magiky. Trvá asi do počátku 8. století.</p>

<p>Dále jsou uvedena nejznámější díla:</p>
<p class="ui">
<em class="autor">Andante Lighierian</em> (asi 602 - asi 667)<br>
<strong class="dilo">Fraška ďábelská</strong><br>
Dílo ukazuje cestu životem na počátku novodobí. Hlavní postava Andante stíhán zlem i dobrem prochází věčně opuštěn světem a míří ve věčný spánek. Dílo výrazně poznamenala doba doznívajícího stíhání magiků.
<br></p>
<p class="ui">
<em class="autor">Franka Petra</em>(asi 680 - 714)<br>
<strong class="dilo">Já, píseň</strong><br>
sbírka básní a písní nešťastného mladíka s podivným jménem. Historikové literatury tvrdí, že jeho otec byl stíhán za čarodějnictví, tím je prodchnuto celé dílo. Nacházíme zde též první známky počínajících vlivů stříbrověku.
</p>

<h3>Stříbrověk</h3>
<p>
Období počátku ranného stříbrověku nalézáme v 8. století, kdy byla objevena rozsáhlá ložiska stříbra na mnoha místech kontinentu. Začínají vznikat ekonomické rozdíly mezi jednotlivými státy. Společnost se vzchopuje, do jejího čela se dostávají po bok šlechty i kněží.
Vrcholný stříbrověk přináší velký rozvij nemagické literatury a kultury (zavržení magie a odmítnutí její prosté existence předními filozofy doby). Ekonomika většiny států sílí, zlepšuje se život společnosti a dochází k rozšíření vzdělané vrstvy.
Pozdní stříbrověk navazuje na vrcholný jen s malými změnami nadcházejících nových časů sjednocování. Končí počátkem 11. století vznikem Federace.
<br><br>
Dále jsou uvedena nejznámější díla:
</p>
