<h1>Intoleranční patent</h1>
<p>(Vydán federativním sněmem 1. dne 1. týdne Zimy roku 1430 na základě jednání federativní rady z Podzimu 1429.)</p>
<p>Na základě tohoto federativního zákona vcházejícího v úplnou platnost 1. dne 1. týdne Zimy 1430 je mimo zákon postavena příslušnost k níže uvedeným církvím a kultům:</p>
<ul>
<li>Druidi lesů, vyznavači bohyně přírody Natürwanny</li>
<li>Vyznavači Cesty krve boha Haemera</li>
<li>Kult těla vyznavačů boha Saluse</li>
<li>Kult čisté pravdy vyznavačů boha Sciolla</li>
<li>všichni vyznavači Svobody, bohyně Libartaenny bez výjimky</li>
<li>Zabíječi, vyznavači smrti boha Thanatose</li>
<li>jakékoli nově vznikající extremistické náboženství svými zásadami porušující nebo vedoucí k porušování zákonů federace</li>
</ul>
<p>Tyto církve a kulty svojí ideologií stojí proti dříve vydaným zákonům Lithénské federace, svým učením a jednáním systematicky je porušují nebo navádějí svoje přívržence k jejich porušování. Účelem patentu je předcházení nedodržování federativních zákonů z důvodu přesvědčení. Ostatní církve budiž Lithénskou federací uznány. Všichni vyznavači zakázaných církví nechť je beztrestně opustí a přestanou jejich existenci jakýmkoli způsobem podporovat k 1. dni roku 1430, nebo budou trestáni podle zákonů federace (vězením či v krajním případě i trestem na hrdle). Z platnosti tohoto zákona jsou některé části Lithénské federace vyňaty.</p>