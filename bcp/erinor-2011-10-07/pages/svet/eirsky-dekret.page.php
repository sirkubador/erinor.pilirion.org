<h1>Obecně známé výňatky z Eirského dekretu</h1>
<p>
<strong>I.I</strong> Zástupci všech zúčastněných stran, tj.Alderachu, Mezihorska, Ragadornu, Dhargetského středozemí, Nolgithských zemí, Irothu, Království Albirey a ostrova Eir, jednoznačně popírají jakoukoli možnost existence dosud nevysvětlených domělých jevů označovaných jako magie, či kouzla.
</p><p>
<strong>I.II</strong> Osoby, kteří se stanou svědky jakéhokoli nevysvětlitelného jevu jsou povinni to neprodleně ohlásit představenému církevní moci v dané oblasti. Porušení může být trestáno.
</p><p>
<strong>I.III</strong> Jakékoli šíření bludů ohledně nevysvětlitelných jevů či jejich popisování jako magie nebo kouzla je tresáno podle závažnosti provinění.
</p><p>
<strong>I.IV</strong> Tím se zároveň nakazuje ničení všech listů a dokumentů obsahující zmínky o podobných bludech.
</p><p>
<strong>I.V</strong> Každý, kdo takovéto spisy vlastní nebo přechovává je automaticky brán jako majitel těchto listů a musí prokázat opak.
</p><p>
<strong>I.VI</strong> Kvůli dodržovýní výše zmíněných pravidel byla zástupci zúčastněných stran ustanovena instituce zabývající se zmíněnou problematikou.
</p>
