<h2>Larpově-divadelní večer</h2>
<p class="r bez ui"><strong class="verze">Organizují: Pilly, Betty</strong></p>
<p>Jako opravdová třešnička na dortu je připraven zhruba tříhodinový večerní program, který si nemůžete nechat ujít. Program je připravený jak pro účastníky erinoření, Erinoru i pro další zájemce. 
Pojďte se podívat na divadlo, z kterého si odnesete zážitky jako z žádného předtím. Zjistíte, že mezi larpem a divadlem je jen tenká hranice, kterou lze překročit.
</p>
