<h2>Komorní larp Bodlák</h2>
<p class="r bez ui"><strong class="verze">Organizují: Balda, Pilly, Spell</strong></p>
<p>Bodlák, to je osoba, která vám dnes ráno nesedla. No možná vám nesedí vůbec, ale to není důležité - prostě roste a je urputně náročné se jí zbavit. A jak tomu osud chtěl, většinou se podobné schází s rozdílným na malé ploše. A zde se také odehrává náš příběh. Snad je to vše shoda náhod a nebo osud. Ale zatím nikdo nic netuší, všude vládne nevraživost , strach, nedůvěra a možná i minulé problémy.. 
</p><p> Ale jak to dopadá, když jeden kamarád pozve druhého do hospody a čeká tam na něj kostel. Kromě kostela také márnice a možná i cirkusový stan. Taxík není, ale ráno bude.
</p>
<h2>Postavy</h2>
<p>Pro vaší lepší představu je zde soupis postav. Jejich počet je variabilní a nemusí tak zůstat. Za postavy se nepřihlašujte, jejich rozdělení proběhne před samotným larpem.
</p><p>Antonín - 38 let, manžel a otec; Má enormí zájem nepřijít o své vysoké společenské postavení.
</p><p>Petr - 35 let, manžel a otec; Je vlivný a má velkou moc, dovede být na své spolupracovníky opravdu zlý, ale přesto je o něm známo, že k dětem se chová velmi vlídně. 
</p><p>Marie - 19 let, dcera Petra; Mladá dívka plná předsudků vláčená osudem, který k ní není zrovna přívětivý. 
</p><p>David - 20 let , syn Tomáše; Na rozdíl od svého velkého vzoru on žebřík nepotřebuje, kvůli jeho zálibě mnozí nechápou jeho životní hodnoty.
</p><p>Tomáš - 45 let, hospodský; Každá škodná se musí odstranit, i ta v lese, i ta v ložnici.
</p><p>Julie - 38 let, manželka Antonína; Matka dvou synů z nichž jeden je mrtvý a jeden má mnohá tajemnství.
</p><p>Pavla - 34 let, manželka Petra; Je důkazem, že pro štěstí své vlastní krve obětuje matka cokoli.
</p><p>Vojtěch - 22 let, syn Antonína; Má rád les, zvláště z toho důvodu, že jak je známo není do něj vidět. 
</p><p>Vít - 33 let, opilec; Milovník dobrých mravů a dobrého alkoholu.
</p><p>Edita - 38 let, sestra Petra; Protloukala se životem jak to jen šlo. Ale po návratu domů, stále nenachází odpovědi. 
</p><p>Radovan - 36 let, otec; Rozpolcená osobnost, která se nikdy nedovede rozhodnout co je správné a hlavně včas. Brání mu v tom leckdy předsudky, kterými rozhodně situaci nezlepšuje. 
