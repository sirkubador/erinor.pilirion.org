<h2>Prolog</h2>
<p>
Takto nevyzpytatelné počasí nepamatovali obyvatelé baronství už po mnoho let. Závěje sněhu hned tály, hned zas tuhly mrazem, který svojí silou a nečekaností zabíjel dobytek ve chlévech i zvěř v lesích. Šestého týdne Zimy už to vypadalo, že sníh snad zmizel nadobro, ale samozřejmě tomu nikdo nechtěl věřit. Přesto mnoho poutníků využilo oblevy a teplého počasí a vydali se na další, ač nesporně riskantní cestu.
</p><p>
Míjel osmý týden Zimy nového roku 1430.
</p><p>
V zimním sídle barona Darena se rozléval zimní poklid, oheň hořel ve všech kamnech i krbech a příjemně zaháněl zimu zpět ven do bouřlivé ledové noci. Baron klimbal v křesle u krbu, jeho krásná snoubenka seděla ve druhém a cosi četla v objemné knize. V tom se ozvalo tiché a nesmělé zaklepání a po vyzvání vstoupil do místnosti sloužící. Tiše spravil svoji paní s tím, že několik poutníků zastižených na cestách krutou ledovou bouří prosí co nejponíženěji pána Barona o přístřeší&hellip;
</p>