<p>V Lithénské federaci jsou čtyři základní platidla</p>
<ul>
<li>Trojníky</li>
<li>Groše</li>
<li>Zlatníky</li>
<li>Tolary</li>
</ul>
<p>Vypráví se, že kdysi měl každý národ dnešní Federace své mince, krásně propracované s různými výjevy z heraldiky, kresbami panovníků a dlouhými opisy. Pak však přišly neblahé události, které podnítily vznik Eirského dekretu a po jeho vydání dlouhá desetiletí válek. Králové, vévodové, knížata, baroni a vůbec všichni tehdejší místní vládci potřebovali velké množství malých laciných mincí a tak začali razit mince tak mizerné, že časem z nich veškeré výjevy zmizely.
</p><p>Staré krásné mince byly zakládány do sbírek a postupem času se ztratily docela. Nadešel čas míru a staré měny států byly zapomenuty, zůstali pouze mince války. Na základě dohod o míru se ujednotila tato měna, měla připomínat zlé časy. Svět, který zapomněl na krásu starých mincí se s tím spokojil. Obchodníci byli rádi za jednotnou měnu, pro krále byla tato ražba také výhodná a hlavně levná. A tak se tyto čisté mince ujaly a platí do teď. A když se začaly slučovat první státy ve dnešní Federace, nikdo už si na stará platidla ani nevzpomněl.
</p>
