<h1>Války za federace</h1>
<ul>
<li>1. sjednocovací válka – utvořila se tkz. Menší konfederace. Byla spíš vedena na poli diplomatickém. Všechny státy Menší konfederace se staly součástí dnešní federace v podstatě dobrovolně jen pod pohrůžkou napadaní.
</li>
<li>2. Sjednocovací válka – Menší konfederace se dohodla že ve stmelování státu bude pokračovat až do té doby dokud nebudou součástí společného uskupení všechny státy ve známém lidském světě. V této druhé fázi byly připojeny hlavně státy na jihu – Ragadorn, Darget a Alderah. 
</li>
</ul>

<strong>Války o trůn</strong>
<p>Tento název pod sebe zahrnuje několik otevřených konfliktů, vražd i jiných politických zásahů v průběhu jednoho století ovlivňovali chod celé federace. </p>
<p>Linie vládců se odvozovala od Raena Sjednotitele, který po druhé sjednocovací válce stanul v čele utvořené federace. Trůn se dědil na principu seniorátu – vždy nejstarší potomek Raena v mužských liniích. Po několika generacích už ale byla situace okolo nároků na trůn tak nepřehledná, že často bylo více uchazečů o trůn a nebylo možné dohledat čí nárok je lépe podložený. </p>
<p>Vznilo několik hlavních linií královského rodu a ty mezi s sebou soupeřili o moc. Vzájemné vraždy a politické intervence vedli k tomu, že se nakonec ztenčil výběr pouze na svě dědičné linie – rodinu niktosů a  guidovců.</p>
<p>Spory mezi bratrskými dynastiemi a jejich neustále soupeření o moc nakonec vedli k ustanovení dynastie adoptivních císařů – císař je volen předchozím. Titul se nedědí v přímé linii.</p>
<strong>Období Velkého rozkvětu</strong>
<p>Ihned po nastolení dynastie adoptivních císařů došlo k rozvoji měst, zlepšení stavu federativních i státních financí k investicím do infrastruktury a obchodu. Navýšil se počet obyvatel. Rostla města, stavěla se nová.</p> 
