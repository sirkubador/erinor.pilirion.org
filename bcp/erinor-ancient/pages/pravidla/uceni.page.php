<h1>Učení</h1>
<p>Novým schopnostem se může postava učit několika způsoby:</p>
<ul>
<li>od postavy (učitele), který může některé schopnosti vyučovat<br>
Samotné učení pak probíhá tak, že postava vyhledá učitele, který umí učit danou schopnost. Ten musí být ochotný postavu vyučovat. Objasní hráči podmínky, při kterých je to možné (počet Skillpointů, nutná výše atributů,atd.), a dohodnou se na ohodnocení (sumě peněz, úkolu, apod.). Hráč zaplatí a odečte si potřebný počet Sp. Pak probíhá samotná herní výuka, na jejímž konci hráč dostane pravidla pro danou schopnost a může jí začít používat.</li>
<li>z nalezených dokumentů (většinou to na nich bude poznamenáno), v tomto případě je lepší se ujistit u organizátora, kde dostanete i podrobná pravidla pro danou schopnost</li>
<li>v jedné z institucí k tomu určených (<a href="pravidla/university/">university</a>, <a href="pravidla/knihovny/">knihovny</a>, atd.)</li>
</ul>
<p>Pokud vaše výše atributů nedosahuje požadovaných hodnot, znamená to, že učení dané schopnosti bude pro vás obtížnější. Každý bod, o který je výše vašich atributů nižší než požadovaná hodnota, musíte "doplatit" jedním Sp navíc. Pokud vám na naučení schopnosti chybí několik bodů Ap u více schopností - každý chybějící bod musíte doplatit Sp navíc.</p>