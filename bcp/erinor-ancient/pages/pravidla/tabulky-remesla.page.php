<li><strong class="schopnost">Výroba nástrojů</strong> <em class="spcena">(1sp)(3 odl + 2 int)</em>
   <ul>
   <li>Krumpáč – HV 10 trojníků, 1 jednotka dřeva, 1 jednotka železa – krumpáč sníží dobu potřebnou na  vytěžení jedné jednotky v dolu o 1 min. </li>
   <li>Lopata – HV 8 trojníků, 1 jednotka dřeva, 1 jednotka železa – lopata sníží dobu potřebnou na  vytěžení jedné jednotky v dolu o 1 min. (nelze sčítat s krumpáčem)</li>
   <li>Motyka – HV 8 trojníků, 1 jednotka dřeva, 1 jednotka železa – sníží dobu potřebnou pro vypěstování jedné rostlinky na zahrádce o 30 min.</li>
   <li>Pila – HV 10 trojníků, 1 jednotka dřeva, 1 jednotka železa – sníží dobu potřebnou k získání 1 jednotky dřeva v lese o 1 min.</li>
   <li>Sekera – HV 14 trojníků, 1 jednotka dřeva, 1 jednotka železa – sníží dobu potřebnou k získání 1 jednotky dřeva v lese o 1 min (nelze sčítat s pilou)</li>
   <li>Kladivo – HV 8 trojníků, 1 jednotka dřeva, 1 jednotka železa </li>
   <li>Rýžovací splav – HV 16 dřeva – rýžovací splav je nutný k používání schopnosti rýžování</li>
   <li>Prut – HV 8 trojníků, 1 jednotka dřeva– zkracuje dobu potřebnou k lovu ryby o 3 min.</li>
   <li>Sítě – HV 12 trojníků, - zkracuje dobu potřebnou k lovu o 5 min nebo je lze použít jako pouta 1</li>
   <li>Úl – HV 10 trojníků, 3 jednotky dřeva – umožňuje používání herní schopnosti včelařství</li>
   </ul>
</li>
