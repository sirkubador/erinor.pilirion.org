<h1>Pravidla pro noční hru</h1>
<h3>Zbraně</h3>
<ul>
<li>v noci jsou povolené pouze zbraně do 90cm (dýka, tesák, jednoruční meč a pádné zbraně)</li>
<li>je zakázáno používat kombinace přesahující 90cm</li>
<li>štíty jsou z nočních soubojů vyřazeny úplně. Postavě, která má malý štít a nemůže jej v noci používat, přibude 1 život. Střední štít - 2 životy. Velký štít - 3 životy.</li>
<li>veškeré střelné (prak, luk, kuše) a vrhací zbraně jsou z nočních soubojů vyřazeny úplně.</li>
<li>toporo pádných zbraní musí být v dostatečné délce přibaleno</li>
<li>všechny zbraně, které chcete používat, přibalte - v noci nezáleží na vzhledu, ale na bezpečnosti.</li>
</ul>
<p>Zbraně budou přísně kontrolovány a veškeré nevyhovující nebo nebezpečné zbraně budou zabaveny a odevzdány majiteli po skončení hry. Výjimky z těchto pravidel mohou být uděleny zbraním, u kterých organizátoři vyloučí možnost vzniku jakéhokoli zranění.</p>