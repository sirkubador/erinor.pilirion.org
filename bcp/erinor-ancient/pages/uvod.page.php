<p>Erinor je rozsáhlý larp typu svět, který po více než deseti dílech a pěti letech otvírá novou kapitolu. Čeká na vás velký prostor pro vaši postavu i realizaci nápadů a představ. LARP je vhodný i pro úplné nováčky, i když rozsáhlý svět je určený především zkušenějším.</p>
<p>Snažíme se zaměřovat hru na dva odlišné aspekty: Pro zájemce je připravený poklidný život každého člověka ve společnosti (obchod, řemesla, politika). Druhým aspektem je rozsáhlé „pozadí“, záhady, atmosférické a vypjaté události.</p>
<h2>O hře</h2>
<p>Erinor je o životě obyčejných lidí. Přece skoro každý se chce stát dobrým a vyhlášeným řemeslníkem, bohatým obchodníkem, potulným mnichem Ater nebo léčitelem. A nebo taky ne!
Na první pohled klidné místo plné obyčejných lidí a jejich osudů, co víc si přát.</p>
<p>Snad jen pár rad do života. Je třeba si dávat pozor na to, že ne vše je tak jednoduché jak se zdá, ne každý je nepřítel, ne každá společnost je přesně to pravé pro vás, ne každá náhoda je dílem bohů a ne každá legenda je pouze smyšlená.</p>
<p>Teď věcně: Erinor je larp hodně o vztazích různých skupin lidí a jejich ambicích. Je o boji mezi jednotlivými cechy, boji gardy proti černém trhu a pašeráctví, boji inkvizice proti zbytkům umění magie, boj jednotlivých náboženství o moc a zachování řádu, atd.
 Ve hře je také mnoho více či méně důležitých záhad a postav, které z nějakého neznámého důvodu prahnou po vědění. Na bestie, magické tvory a artefakty  při procházce opravdu nenarazíte, spíš tak na obchodní karavanu nebo léčitele sbírajícího bylinky.</p> 
<p>Během roku se většinou odehrávají dva herní díly - hlavní (Svět) a mezidíl. Každý má vazbu na ten předchozí, i když to nemusí být očividné. Mezidíl je většinou o určité situaci, do které se hráči svým konáním dostali.</p>   
<h2>Jak začít</h2>
<p>Protože již existuje k Erinoru velké množství různých dokumentů, je těžké se v nich zorientovat. Zde je v pár bodech napsáno jak postupovat a co nejsnáze se seznámit s herním světem.</p>
<ul>
   <li>V sekci <a href="svet/">Svět</a> si přečtěte - Základní popis herního světa, Eirský dekret, Náboženství a intoleranční patent. Přečtení těchto dokumentů je nezbytné, abyste si vytvořili představu o herním světě a mohli se dát do vymýšlení své postavy.</li>
   <li>Veškeré informace o budoucím dílu hlavně o jeho prologu a herní situaci najdete v pořádaných. </li>
   <li>Dále si přečtěte základní pravidla - ty je nutné si opravdu dobře pamatovat. S pomocí ostatních pravidel byste si měli vytvořit představu o své postavě.</li>
   <li>Čtení jakýchkoli dalších herních dokumentů je již jen na vás. Dá se tím ale získat množství cenných informací. </li>
   <li>Komukoli s postavou rádi pomůžeme a rádi ho herně zaměstnáme. Napište nám to, ale do přihlášky. Uveďte přesto alespoň hrubou představu co by vaše postava mohla dělat.</li>
   <li>Erinor je otevřený i úplným nováčkům. S většinou z nich se rádi setkáme osobně a pomůžeme jim s přípravou. Počítejte s tím, že příprava na Erinor je náročná, ale uvidíte, že se vám to vrátí.</li>
    </ul>   
</p>
<h2>Přihlašování</h2>
<p>Hra je určena pro starší 15 let. </p>
<p>V přihlášce uveďte celé jméno, přezdívku a věk. Dále vaši představu o postavě. Schopnosti, vlastnosti, které byste chtěli mít a krátký popis kostýmu.</p>
<p>Povinnou součástí přihlášky jsou také cíle. Zajímá nás jak ambiciózní vaš postava bude. Jak by toho chtěla dosáhnout a pod.</p>
<p>Pokud k přihlášce přiložíte historii postavy (ideálně do A5), fotku kostýmu a případně výčet vašich rekvizit, které by měly dotvářet vaší roli, budou vám rovnou přiděleny bonusové body a můžete je po domluvě rovnou použít.
Historie vaší postavy by měla být zaměřená na fakta nikoli na uměleký dojem. Cokoli dalšího je otázkou domluvy - jen je třeba začít se domlouvat včas.</p>
