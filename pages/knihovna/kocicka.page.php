<h1>Kočička</h1>
<p>Tmou brzkého rána, toho bezútěšného času před úsvitem, přelétla přes stezku podivná bytost, stín či mámení. Pak se na cestě objevila černá kočka se žhnoucíma zlatýma očima.
</p><br>
<p>Tři dobrodruzi vyráželi na cestu. Noc se zvolna chýlila k ránu. Před dalekou poutí se vesele smáli a halekali, když opouštěli dům posledního z nich kdesi daleko za krajem města. Naložili vaky na záda a vykročili do tmy.
</p><p>Sotva minuli poslední uličku a vstoupili do lesa, přes cestu před nimi proběhla černá kočka.
</p><p>"Hele jí!" pravil první z dobrodruhů, malý učenec a alchymista. "Tfuj, a třikrát tfuj před neštěstím!"
</p><p>"No jo, kočka. Tak to budeme mít dobrou snídani," dodal druhý, šermíř a bard bohatýrského chování.
</p><p>Zvíře se mu připletlo pod nohy a on je nevybíravě nakopl až odlétlo. Než však minulo pár okamžiků, kočka už znovu běžela po cestě před dobrodruhy.
</p><p>"Tak by mě zajímalo, jak dlouho s námi půjde," pravila třetí, kněžka s pohledem upřeným někam do tmy.
</p><p>"Asi do té doby, než jí chytneme a upečeme," řekl bard.
</p><p>"Ale no tak, to přece nemůžete," oponovala kněžka.
</p><p>"Byla by to moc dobrá snídaně. Kožešinku bych vydělal a ocas nosil za kloboukem," dodal alchymista. To doprovodil smích zbylých dvou dobrodruhů.
</p><p>Kočka si, jak vidno, z řečí mnoho starostí nedělala. Jak by snad také mohla, že? Dál klusala pár kroků před nimi. Co chvíli se pletla někomu pod nohy, tiše mňoukala a podivně vlnila ocasem.
</p><p>"A že se mě nějak drží," nadhodila kněžka. "Proč se pořád motá pod nohy právě mně?"
</p><p>"Třebas je to kocour," zasmál se alchymista.
</p><p>Kněžka se musela rdít, ale smála se též.
</p><p>Pomalu svítalo, už notný kus cesty minul dobrodruhům za zády ve veselé rozpravě a to prapodivné zvíře stále neúnavně běželo před nimi.
</p><p>"Možná nás někam vede." Kněžka se cítila trochu nesvá, cosi zjevně nebylo v pořádku, co pravilo jí její podivínské vědomí.
</p><p>"Leda tak do záhuby," smál se dál lehkovážně bard a pískal si vesele do kroku.
</p><br>
<p>Tak minulo pomalu ráno a z podzimních mlh vylouplo se poledne.
</p><p>Nekonečně dlouhý hvozd se táhl přes půl dne chůze do všech stran.
</p><p>"Co, že nám našla i dobré ohniště, kde si tu potvoru můžeme upéct," zahalasil alchymista.
</p><p>"Nu, což, je to jen kočka," nakonec i kněžka svolila, když došli k cizímu ohništi nejspíše z předminulé noci. "Bude to tak. To nás vedou bohové k dobrému obědu," usmála se.
</p><p>Složili vaky a alchymista počal rozdělávat oheň. Bard, v jedné ruce dýku, vzal zvíře lísající se k nohám kněžky.
</p><br>
<p>Když pak chutné masíčko olizovaly plameny, alchymista je natíral bylinkami. Kněžka hledala cosi v mohutné knize a bard ladil loutnu. Oheň tak příjemně zahříval nohy, že se rozhodli na další cestu vyrazit až ráno.
</p><br>
<p>Ve vesnici na kraji hvozdu vyprávěl příštího večera jeden z lovců prazvláštní zvěst. Našel prý na místě svatých ohňů druidů na den starý popel, vaky několika dobrodruhů a - lidské kosti, doběla obrané rozházené kolem jak po hostině.
</p><p>A u ohniště stála podivná kočka, černější, než podzimní půlnoc s očima tak zlatýma, že žhnuly víc, než plameny ohňů svátků dlouhonoci.
</p>