<h1>Postavení kněží ve světě erinoru</h1>
<p>Kněžská moc je přísnou a neporušitelnou hierarchií. Hlavou kultu je vždy jediný patriarcha/matriarcha kultu, který je nejvyšší představitelem církve pro celou federaci. Ten má svého náměstka – nejvyššího představeného v každém ze států federace.
</p><p>Nejvyšší představený dohlíží na liturgiku a zbožnost v zemi s tím mi pomáhá sbor velekněží –alespoň jeden je vždy v každém větším městě. Ti mají na starosti velké chrámy a vše, co se děje kolem nich. Dohlížejí na obřady, kněží a zasvěcence. 
</p><p>Ti nejvyšší kněží se podílejí na správě a chodu státu – tvoří tajnou radu – vyšší poradní sbor císaře a ten na jejich rady a doporučení velmi dbá. 
</p><p>Všichni kněží jsou evidovaní. Archiv je v hlavním městě v paláci patriarchů. Všichni kněží jsou jednou za rok povinni poslat do archivu svoji momentální pozici a kde šíří slovo boží. 
</p>



    

    

    

    
