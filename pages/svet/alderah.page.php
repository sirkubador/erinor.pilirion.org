<h2>Alderah</h2>
<p>
<strong>Alderaššké baronství</strong> &ndash; zemědělská oblast. Díky poloze mezi horami má stabilní klima vhodné pro pěstování odolnějších plodin. Pěstují se obilniny a odolnější zelenina. Z ovoce pouze jablka, slivoně a hrušky. 
</p><p>
<em>Vztah k Lithénské federaci</em> &ndash; Alderah je k federaci loajální. Vždyť je její obilnicí a díky tomu má i své výhody, kterých umí docela využít.
</p><p>
<em>Vztah k sousedním státům</em> &ndash; Vztah Alderahu s Ragadornem je lehce řevnivý. (viz <a href="svet/ragadorn/" title="Ragadornské království (Ragadorn)">Ragadorn</a>)
</p><p>
<em>Místní názvy a jména</em>: podobají se slovanským
</p><p>
<em>Po válce s elfy</em>: Alderah z války vyvázl celkem dobře. Země i obyvatelstvo je sice trochu vyčerpané, protože po 10 let živili velkou armádu a během velké mobilizace bylo odvedeno mnoho mužů i odsud. Politicky ale Alderah posílil, uzavřel během války několik výhodných smluv s Ragadornem. Současný baron se oženil se sestrou krále Ragadornu a pokud by ten zemřel bez dědice, usedl by on nebo jeho syn na ragadornský trůn a obě země spojil.
</p>
