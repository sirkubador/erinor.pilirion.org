<h1>I. dodatek intolerančního patentu</h1>
<p>1. den 1. týdne Léta roku 1434
</p><p>Na základě výnosu federativního sněmu vchází 1. dne 1. týdne Léta roku 1434 v platnost dodatek k intolerančnímu patentu striktně zakazující tyto kněžské praktiky z důvodu jejich potenciální nebezpečnosti:
</p>
<ul>
<li>1. Obětování</li>
<li>2. Velké obřady prováděné nedostatečně pověřenými kněžími</li>
</ul>
<p>Kněz nesmí provádět bohoslužby a obřady neadekvátní jeho postavení v církvi. To musí být ověřeno dokladem vydaným nejvyšším představitelem církve nebo jeho zástupcem, který může udělit výjimku z následujícího pevně daného rozpisu církevní hierarchie:</p>
<ul>
<li>Nižší zasvěcenec – soukromá modlitba, nepotřebuje doklad</li>
<li>Kultista vyššího zasvěcení – obřad pro nejvýše 5 věřících, nepotřebuje doklad</li>
<li>Novic – obřad pro nejvýše 10 věřící</li>
<li>Kněz – obřad pro nejvýše 20 věřících</li>
<li>Velekněz – neomezeně</li>
<li>Nejvyšší představený – neomezeně, nepotřebuje doklad</li>
</ul>
