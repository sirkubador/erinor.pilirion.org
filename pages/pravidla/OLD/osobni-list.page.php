<h1>Osobní list</h1>
<p>Při každé nové registraci obdržíte osobní list (dále OL), do něhož jsou zapisovány veškeré údaje, které charakterizují vaši postavu a umožňují tak přehled vašich dovedností, vlastností a shrnují poznámky nutné ke hře. Jedná se o neherní věc, sloužící čistě pro vaše a organizátorské účely - tudíž žádný jiný hráč nemá právo do vašeho OL nahlížet. Ve hře ho musí mít každý hráč u sebe!
</p>
<p>Zde je verze <a href="materialy/osobak-final.pdf">osobního listu pro tisk</a> (PDF)</p>
<h2>Jednotlivé položky OL</h2>
<dl>
<dt>Jméno</dt><dd>"herní jméno"</dd>
<dt>Titul</dt><dd>udává vaše společenské postavení a také možné tituly s tím spojené</dd>
<dt>Reg</dt><dd>Jedná se o soubor poznámek důležitých převážně pro organizátory. Obsahuje také rasu vaší postavy a její původ.</dd>
<dt>Stav</dt><dd>Obsahuje veškeré údaje o vašem postavení - cechovní a církevní příslušnost, a samozřejmě i to zda nejste náhodou herně ženatý/vdaná apod.</dd>
</dl>
<h3>Atributy vaší postavy (AP)</h3>
<dl>
<dt>Síla</dt><dd>Je nutná především na boj z blízka a definuje možnost zacházet s různými zbraněmi (a schopnosti s tím spojené).</dd>
<dt>Odolnost</dt><dd>Charakterizuje vaši výdrž. Je potřebná pro velmi namáhavé schopnosti, nošení zbrojí, atd.</dd>
<dt>Obratnost</dt><dd>Důležitá pro zručnost ve výrobě, tak i v používání střelných zbraní. Je potřebná pro některé dovednosti.</dd>
<dt>Inteligence</dt><dd>Vhodná ke studiu religionistiky, umění, léčitelství, atd.</dd>
</dl>
<p>Na začátku má každý 9 bodů a může je libovolně přidělit k některým z atributů. Avšak žádný nesmí mít hodnotu 0 (minimálně 1). Atributy se během hry vyvíjí a to značně vzácně a ne vždy požadovaným zvyšováním. Horní hranice neexistuje, ale některé atributy vylučují jiné. To závisí na hodnotách vašich atributů a vlastnostech postavy.</p>
<dl>
<dt>Životy</dt><dd>počet životů postavy (základní počet jsou 3 + za každé 3 body odolnosti je jeden na víc). Tedy 4. život je na odolnosti 4.
Do závorky k životům si napište počet stínových životů (v norm. případě se shoduje s počtem vašich životů).</dd>
</dl>
<p>Člověk se bez schopnosti neléčí.</p>
<dl>
<dt>Magie</dt><dd>velmi kontroverzní položka - vždyť žádná magie přece neexistuje</dd>
</dl>
</li>
</ul>
<h3>Skillpointy (SP, schopnostní puntíky)</h3>
<p>Charakterizují možnosti postavy učit se novým dovednostem (schopnostem).</p>
<ul>
<li>základní počet SP se odvíjí od zkušeností, které má vaše postava do začátku. Mladé a nezkušené postavy začínají s 9Sp. O to rychleji se ale učí a získávají nové schopnosti. Stařší postavy mají o něco více Sp a schopností, ale jejich učení již není tak rychlé. Ve hře se mohou objevit i staří mazáci.</li>
<li>Jejich získávání není plošné tzn. u všech stejné, ale odvíjí se u každého hráče individuálně.</li>
<li>Jejich rozdělování zařizují IO (informační obláčky), při čemž závisí na obživě, atd.</li>
<li>Základní přidělování SP probíhá jednou za herní den.</li>
<li>Za některá SP je možné se učit pouze schopnost vyžadující daný atribut - např. SP síly je možné použít jen na naučení schopnosti vyžadující sílu.</li>
<li>Tyto specializovaná Sp si označte ve svém Osobním listu, tak že jejich políčko vybarvěte příslušnou barvou:
   <ul>
   <li>SP síly žlutě</li>
   <li>SP odolnosti červeně</li>
   <li>SP obratnosti zeleně</li>
   <li>SP inteligence modře</li>
   </ul>
</li>
</ul>
<dl>
<dt>Schopnosti</dt><dd>Soubor poznámek o již naučených schopnostech, jejich účincích, atd.</dd>
<dt>Vlastnosti</dt><dd>Jsou dovednosti, kterým se postava nemusela učit, ale odvíjí se od její profese, předchozího života,apod. + mohou zde být popsány i charakterové vlastnosti postavy. Více se dozvíte v části pravidel <a href="pravidla/vlastnosti/">vlastnosti</a></dd>
<dt>Poznámky</dt><dd>Tady je prostor pro jakékoli poznámky, vysvětlování účinků, modlitby, pravidla, informace, atd.</dd>
</dl>
