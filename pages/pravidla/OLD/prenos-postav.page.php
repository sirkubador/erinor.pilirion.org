<h1>Přenos postav</h1>
<p>Přenos postav do dalšího dílu je možný. Vždycky když chcete mít svojí postavu v dalším díle, dohodněte se s organizátory. Jediná vyjímka, kdy postavu nelze přenést je pokud by to nedávalo smysl (určí orgové). Mezidílové učení, získávání herních předmětů a podobně není moc běžné a budem ho povolovat jen výjmečně.</p>
