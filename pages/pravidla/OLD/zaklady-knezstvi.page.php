<h3>Poznámka pro tvořivé</h3>
<p>Samozřejmě si můžete vymýšlet vlastní intervence, jenom si je musíte
nechat před tím schválit. Vymyšlení intervence musí proběhnout i herně
(úkol od IO, př. Hezky provedený rituál na zadané téma).</p>

<h3>Pomůcka pro omezení počtů seslání</h3>
<p>V pravidlech se vyskytují pouze 3 typy omezení počtu seslání.</p>
<ol>
<li>1x za 3 hodiny (většinou se týkají instantních manipulací s životy)</li>
<li>1x za den (omezení mocných jednorázových intervencí)</li>
<li>bez omezení, nebývá uvedeno, kolikrát sesilatel chce (a na kolik má interce)</li>
</ol>
<h2>Obecné schopnosti</h2>
Tyto schopnosti získává kněz se základním kněžstvím.

<h3>Provedení rituálu</h3> 
<p>Trvá nejméně 5 minut s patřičnými efekty (vzývání, pronášení sesílací formule, gesta...). Rituál je potřeba pro sesílání některých intervencí (označených V) – tzv. Vyvolávaných.
</p>
<h3>Vysvěcení svatého místa</h3>
<p>Takto si kněz může sám po alespoň 10 minutovém rituálu kterého se aktivně účastní alespoň 2 věřící na patřičném (a IO schváleném) místě vytvořit svoje svaté místo. Kněz může mít jen jedno svoje svaté místo (pokud je jím vysvěcené místo zanikne nebo je znesvěceno může si vytvořit nové).
</p>
<h3>Znesvěcení svatého místa</h3>
<p>Kněz provede alespoň 5 min. rituál na posvěceném místě urč. náboženství, které je s jeho v rozporu, a tím zruší posvěcení
</p>
<h3>Snímání raritních intervencí</h3>

<p>Pokud chcete sejmout intervenci, která byla seslána mimo běžná pravidla, poraďte se s IO.
</p>
