<h1>Kněžství</h1>
<p>Kněžství je soubor schopností umožňující komunikovat s bohy a
zprostředkovat tak jejich zásahy do herního světa. Může je využívat
kdokoliv, kdo dosáhl alespoň vyššího zasvěcení ve své víře. Množství
schopností je ale tak veliké, že se postupem času vytvořily specializované
větve, každá se čtyřmi okruhy síly, z nichž jsou kromě <strong>základního kněžství</strong>
(žehnání, uřknutí a jednoduchých obřadů) nejvýznamnější tyto:
</p>
<ul>
<li>Posílení</li>
<li>Oslabení</li>
<li>Léčení</li>
<li>Ničení</li>
<li>Ochrana</li>
<li>Očištění</li>
<li>Odhalení</li>
<li>Zmatení</li>
<li>Posvěcení</li>
<li>Proklínání</li>
</ul>

<h3>Interce</h3>
<p>K jejich užívání slouží přímluvy u bohů &ndash; <em>interce</em>.
Jedná se vlastně o svolení k použití vyšší síly (tedy božské) na různé
úkony. Jejich počet není omezen a jejich získávání je volné a závisí na
několika faktorech. Interce je možné získat za modlitby (tedy veřejné),
bohoslužby, za počty věřících a další církevní obřady (řeší individuálně IO).
Denní příděl intercí je odvislý od moci kněze<br>
<strong>[(počet proučených SP v kněžství + INT) / 2]</strong><br>
Interce, které si kněz obstará mimo denní příděl (bohoslužba/obětování)
se mu zachovávají do dalšího dne, ne však do dalších dílů (kněz spotřebovává
interce z denního přídělu jako první).
</p>
<p>K uvolnění 1 interce z denního přídělu nebo obřadů/bohoslužeb slouží konzumace 1 větší hrozinky nebo lentilky.</p>

<h3>Intervence</h3>
<p>Intervence je božský zásah. Stejně jako u magie se samotný zásah nazývá kouzlo, u kněžství je to intervence.</p>

<h3>Magie</h3>
<p>Použití magie je jakási úplata bohů. Každý, kdo má magické nadání má
automaticky denní příděl intercí snížen na polovinu, protože bohové chtějí
raději přijímat magii. Samozřejmě je tedy nucen používat k vyvolávání magii
a to místo jedné interce 1 mag. Maximální počet magů, kterými lze uplatit
božstvo je závislý na inteligenci a to, za každý bod inteligence dva magy
(tedy jakoby dvě interce). POZOR, tyto se uvolňují v piškotech!
</p>

<h2>Schopnosti kněze</h2>
<p>Jedná se o schopnosti, které nepotřebují interce (nebo je používají nepřímo), jsou to vlastně pomocné schopnosti související primárně s postavou kněze, mohou mu zlepšovat účinky božských intervencí apod.
</p>
<ul>
<li>Poustevnictví</li>
<li>Liturgika</li>
<li>Vepisování</li>
<li>Obětování</li>
<li>Zázraky</li>
</ul>

