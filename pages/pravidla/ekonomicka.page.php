<h1>Ekonomická pravidla</h1>

<h2>Motivace hráčů</h2>
<p>
Aby si mohl hráč přenést postavu do dalšího dílu musí nahromadit během hry dostatečné bohatství. Čím bohatší je na začátku, tím bohatší musí být i na konci. Bylo by vhodné rozdělit hráče do kategorií jako nuzáci, chudí, střední třída(řemeslníci, chudší obchodníci), boháči.
</p><p>
Ve skutečnosti by měly být tři způsoby jak přenést postavu do dalšího dílu – získat dostatečný majetek, získat nějaké mocné místo, Udělat nějaký opravdu důležitý skutek.
</p>
<dl>
<dt>Nuzáci a chudina</dt>
<dd>Pro chudé je úspěchem už třeba to, že budou mít trvale kde bydlet. Takže trvalý pronájem nebo získání bytu nebo, že se stanou mistry nějakého řemesla nebo alespoň kvalitními statkáři.  
</dd>
<dt>Střední třída</dt>
<dd>
Úspěch na soutěži v tom co dělají. Bylo by dobré kdyby na každém Erinoru byly slavnosti, kde by byl vybírán ten nejlepší předmět. S tímhle by mohli souviset speciální návody na výrobu různých unikátních děl, které by se dali různě získat.
</dd>
<dt>Boháči</dt>
<dd>Pro boháče by úspěchem bylo něco takového jako otevření nové obchodní cesty. Či získání šlechtického patentu nebo jiný obdobně velký počin. V prvním novém díle vlastně boháče ani nechceme.
</dd>
</dl>

<h2>Cechy</h2>
<p>
V případě dostatečně velkých měst se jednotlivé stromy sdružují do cechů: řemeslnického, služebního a výrobcovského.
</p>
<p>Cechy mají určité pravomoci a do značné míry jsou protiváhou šlechty. Řemeslo může provozovat pouze ten kdo má cechovní povolení. Za provozování práce bez povolení cechu jsou tvrdé pokuty. Ekonomické cechy zajišťující svým lidem určité výhody například souzení před cechoním soudem a ne státním až na výjimečné případy.  
</p>

<h2>Přežití v průběhu hry</h2>

<dl>
<dt>Spánek</dt>
<dd>Postava, která nebude spát v domě by měla mít šanci na smrt. Šance by měla být zhruba 25%, aby to bylo dostatečně nebezpečné.
</dd>
<dt>Jídlo</dt>
<dd>
Každá postava musí za den sníst 3 porce jídla. Ten kdo nejedl nedostane expy a nemůže používat určité schopnosti.<br>
Pokud nesní jednu porci bude do dalšího jídla oslabená. Přijde o jeden život a některé náročné činnosti nebude schopna vykonávat. Pokud nesní I druhou po sobě jdoucí porci nesmí vykonávat další činnosti a získává 10% pravděpodobnost smrti hladem Pokud nesní 3tí porci je již jen málo věcí, které postava zvládne vykonávat a zvyšuje se pravděpodobnost smrti hladem na 20%. Po čtvrtém nesnědeném jídle už postava je schopná jen se pomalu pohybovat a dělat nenáročné činnosti a má 40% pravděpodobnost smrti. Postava, která nesní 6 po sobě následujících jídel umírá.<br>
Existuje i dobré jídlo, které když osoba sní tak ji vrátí jeden život. V každém intervalu se tímto způsobem dá obnovit pouze jeden život.<br>
</dd>
</dl>

