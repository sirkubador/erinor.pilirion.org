Mistr dřevař

Za stejnou dobu vytěží dvě jednotky dřeva.
Kamenictví

Umožňuje postavě v lomu na kámen těžit kámen. Hráč vytěží každou hodinu jednu jednotku kamene. Stačí aby do lomu přišel a odnesl odtamtud něco málo kamenů do města.
Mistr kamenictví

