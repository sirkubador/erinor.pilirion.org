Hostinský (Kuchař)

Kuchař umí uvařit dobré jídlo. Takové dobré jídlo vyléčí jeden život a nahradí dvě obyčejná. Nicméně není mistrem a tak je možné vyléčit takto život pouze jedním dobrým jídlem za den.
Palič

Postava s touto schopnosti umí pálit alkohol. Na začátek umí uvařit kedlubnovici. V průběhu hry je možné najít recepty na další nápoje. Různé nápoje mají různé efekty.
Mistr Kuchař

Postava mistra kuchaře dokáže s jídlem neuvěřitelné věci. Různá jídla od takovéhoto kuchaře budou mít různé účinky. Každý mistr kuchař může mít své specifické recepty, které nikdo jiný nebude znát.
Výrobce lehčího alkoholu

Postava s touto schopností je schopná vařit lehčí alkohol jako pivo, víno a medovinu či další podobné nápoje na které půjde najít recepty v průběhu hry.
Obchodník

Postava se schopnosti obchodník je schopna každou hodinu jednou usmlouvat cenu po dohodě o deset procent. Neplatí pro obchodování mezi hráči.
Mistr obchodník

Díky mnoha letům praxe v obchodních záležitostech je schopen smlouvat ještě lépe a zvládne usmlouvat až dvacet procent z ceny zboží. Neplatí pro obchodování mezi hráči.
Znalec surovin

Není tak obecně schopný jako mistr obchodník ale o surovinách toho ví opravdu hodně a tak zvládne jejich cenu snížit o třicet procent. Efekty schopnosti se nesčítají. Neplatí pro obchodování mezi hráči.
Znalec zboží

Zvládne snížit cenu zboží vytvořeného řemeslníky o třicet procent. Neplatí pro obchodování mezi hráči.
Herectví

Postava se schopností herectví umí svým hraním trvajícím alespoň 5 minut zaujmout publikum natolik, že ji za to město zaplatí jeden trojník za každého diváka. Divákem rozumíme někoho kdo bude celou dobu sledovat představení. Suma se násobí počtem herců neboli hrají-li dva herci dostanou každý jeden trojník za představení. Tuto schopnost může každý herec použít nejvýše jednou za hodinu.  
Karbaník

Postava s touto schopností umí výborně podvádět při provozování hazardních her. Hraní se dá výborně použít při podvádění. Například při hře v kostky má možnost hodit si kostkou navíc a vybrat si které použije. Při ruletě může dát až jedenapůlnásobek žertonů než by měl standardně vsadit. A další podobné.
Bardství

Postava s touto schopností je schopná postavám, které poslouchají její hře na hudební nástroj nebo jejímu zpěvu vnuknout nějakou náladu nebo emoci - radost, odvahu, smutek. Apriori to ale postavy k ničemu nenutí.                  
Vzdělání

Tato schopnost vám poskytuje znalosti v daném oboru v rámci dosažené úrovně Lithénského vzdělání. Vstup do stromu vzdělání stojí 1 sp. Následují 3 obory – 1. stupně a jejich specializace, 2. stupně. Pokud vám zde něco chybí, domluvte se s orgem.

Pokud máte nějaké vzdělání v daném oboru, můžete používat na dané úrovni svoje znalosti (třeba u přírodovědy, fyziky…) nebo načtené znalosti o světě. Pokud si myslíte, že by při vašem vzdělání měla vaše postava něco vědět, můžete se na to zeptat IO (uvidíte co vám odpoví).


