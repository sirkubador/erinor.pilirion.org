Získávání surovin
Dolování

Umožňuje postavě na vhodných místech dolovat želez. Vhodným místem rozumíme důl na železo. Pro dolování musí postava dorazit do dolu. V každém dolu se jednou za hodinu objeví množství surovin, ze kterých si každý kdo má schopnost dolování vzít 1 odpovídající surovinu.
Dolování drahokamů

Umožňuje postavě dolovat drahokamy. Pro dolování je nutná vhodná prostora, kde si postava může jednou za hodinu vyzvednout drahokam.
Dolování drahých kovů

Umožňuje postavě dolovat zlato, stříbro.
Dřevař

