<h4>Sedláctví</h4>
<p>
Umožňuje dané osobě sklízet úrodu na polích. Pole dají každou hodinu úrodu 1 jednotek jídla každé. V rámci hry existují herní rostliny, jedná se o kartičku se zobrazením rostliny, kterou hráč zasadí, okope a zalije a následně ji nechá růst. Na kartičku při zasazení napíše čas kdy dozraje to jest aktuální čas + 1 hodina. Rostlinu může sklidit kdokoliv se schopností sedlák, nejen ten kdo rostlinu zasadil.  
</p>
<h5>Výroba polí</h5>
<p>Navazuje na sedláctví a umožňuje sedlákovi na vhodných místech zakládat pole. Osoba, která umí zakládat pole může založit 2 nová pole každé 4 hodiny. Pole představuje čtverec 1*1 m, který bude řádně vykolíkován, vyznačen provázkem a bude na něm cedulka, že se jedná o pole.
</p>
<h5>Mistrovství v sedláctví</h5>
<p>Mistr sedlák umí se svými poli pracovat mnohem lépe než ostatní sedláci a dokáže z nich získat každou hodinu 2 jednotky jídla z každého.
</p>

<h4>Sadařství</h4>
<p>K jeho použití je potřeba založení sadu. Následně je možné sad sklízet. Sad může sklízet kdokoliv, pokud si ho vlastník nepohlídá, může mít smůlu. Sad je možné sklízet každou hodinu a tomu kdo jej sklizí přinese dvě jednotky jídla.
</p>
<h5>Zakládání sadů</h5>
<p>Navazuje na sadaření a umožňuje sadaři založit nový sad. Sadař umí založit každé 4 hodiny na vhodném místě sad.
</p>
<h5>Mistrovství v sadaření</h5>
<p>
Mistr sadař umí získat ze svého sadu 3 jednotky jídla každou hodinu, namísto dvou.  
</p>

<h4>Lov</h4>
<p>Pro lov je potřeba mít na něj vhodné náčiní. Lovecké náčiní bude vyrábět truhlář. Nutnou součástí lovu je jeho předvedení. Doporučuji simulovat pokládání pastí pro lov zvěře a tedy koupit nebo vyrobit past a tu donést do lesa a nastražit ji tam. Po hodině se do pasti chytí zvíře, které tam následně jen vyzvedne. Každou hodinu tento způsob vynese jednu jednotku jídla.
</p>
<h5>Mistr lovec</h5>
<p>
Umí vytvářet tak kvalitní pasti, že do nich chytí dvě jednotky jídla. Tato dovednost umožňuje hráči ještě kromě ostatního i ze zvířat stahovat kůži. Kazdou hodinu kdy lovi navíc stáhne i jednu kůži.
</p>

<h4>Rybolov</h4>
<p>Pro rybolov je potřeba vhodné náčiní. Vhodné náčiní umí vyrábět truhlář. Pro lov je potřeba vhodná tekoucí řeka nebo rybník. Jinde ryby chytat nelze. Každou hodinu se pomocí prutu dá ulovit jedna jednotka jídla. Pro rybolov je potřeba dojít ke vhodné vodě a u ní na prut nasadit návnadu, nahodit prut do vody a uchytit ho tak, aby vydržel na daném místě a působil dojmem nahozeného prutu.
</p>

<h5>Mistr rybář</h5>
<p>Věnoval se rybolovu tak dlouho, že zvládne při rybolovu za hodinu ulovit dvě jednotky jídla namísto jedné.
</p>

<h4>Včelařství</h4>
<p>
Umožňuje sklízet pochutiny z herního úlu. Herní úl je možné pořídit u tesaře, ale pro jeho korektní osazení je nutné umět zakládat úly.
</p>
<p>Každý úl produkuje jednu jednotku pochutin za 1hodinu, vyzvednout ji může každou hodinu kdokoliv kdo umí vybírat úly.
</p>

<h5>Zakládání úlů</h5>
<p>
Postava s touto schopností umí úl od truhláře zasadit tak, aby byl k něčemu užitečný. Zařídit, aby ho osídlilo včelstvo a další podobné.
</p>

<h5>Mistrovství ve včelařství</h5>
<p>Takový člověk umí výborně s včelami a díky tomu dokáže z každého úlu získat dvě pochutiny za hodinu.
</p>

<h4>Sběr</h4>
<p>Tato dovednost umožňuje komukoliv každé dvě hodiny po chvíli hledání v lese najít jednu jednotku ovoce.
</p>
<h5>Sběr hub</h5>
