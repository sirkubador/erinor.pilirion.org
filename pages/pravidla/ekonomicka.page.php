Obecná pravidla pro tvorbu.

Každá schopnost hráče naučí některé recepty pro výrobu věcí. Další věci se hráči učí tvořit pomoci receptů, které je možné získat ve hře. Recepty mají zpravidla některou ze schopností jako podmínku pro naučeni.

Výroba každé věci vyžaduje určité suroviny a nějaký čas strávený při výrobě. Každá postava má k dispozici každou hodinu 60 jednotek času, které může využít na výrobu. Je jedno jestli bude vyrábět zbroje nebo truhlařit. Kdykoliv si může postava vyzvednout věci, které dodělala. Doba potřebná pro výrobu věci je udávaná v jednotkách odpovídajících minutám.

Hráč nemusí celý čas strávit výrobou věci. Nicméně by měl zahrát minimálně částečně tvorbu té věci tj. Pokud je kovářem, pak by měl alespoň chvilku hrát kování. Za dobré rp může hráč vytvořit výjimečnou věc.
Vynalézání nových receptů
Každá postava, která umí vytvářet věci, které potřebují recepty má možnost i vymýšlet nové recepty. Není nutné pouze se držet již existujících receptů.

Pro výrobu receptů je potřeba vyrobit vhodnou napodobeninu věci, kterou chcete vyrobit a to za pomocí odpovídajícícho množství surovin, odhadněte to podle zbylých receptů. Tato napodobenina je jen podkladem pro výrobu dalších věcí. Napodobenina musí být fyzicky existující po vyrobení. Tj. pokud byste chtěli vytvořit recept na plamený meč pak musít z něčeho vyrobit napodobeninu plamenného meče. Pokud pro to použijete reálný předmět pak již tento předmět není možné herně použít. U prvních tří použití nového receptu počítejte s 50ti procentní pravděpodobností, že se vám věc nepovede vyrobit.
Kovář

Tato schopnost umožňuje postavě vyrábět drobné kovové předměty jako hřebíky a podkovy a ostřit zbraně. Dále je také pomocí této schopnosti možné vytvářet drobné kovové náčiní jako lopatky, pánvičky a ostatní.  Ostrá zbraň při prvním zásahu způsobí o zranění více.

Pro kováře je důležité vlastnit kovářské náčiní. Je vhodné, aby si hráč přivezl něco na čem může kovat. Může to byt kovadlina nebo třeba větší kladivo.
Výroba zbrani

Tato schopnost je podmínkou pro výrobu zbrani. Kromě toho se postava rovnou naučí recepty na výrobu dýky, tesáku a krátkého meče. Postava s touto schopností umí zbraně také opravovat. Oprava zbraně stojí pětinu času a surovin oproti celému brnění.
Výroba brnění

Tato schopnost je podmínkou pro výrobu všech brnění. Rovnou postavě umožní vytvářet koženou a šupinovou zbroj a malý štít. Postava s touto schopností umí brnění také opravovat. Oprava brnění stojí pětinu času a surovin oproti celému brnění.
Pokovování

Tato schopnost hráči umožňuje zbraň pokovit libovolným dostupným kovem. Na pokovení je potřeba třetina jednotek kovu spotřebovaného při výrobě zbraně.
Zlatník

Tato dovednost umožňuje postavě oceňovat předměty a vyrábět drobné šperky.
Šperkař

Umožňuje vyrábět šperky. A další předměty z drahých kovů.
Truhlář

Tato dovednost umožňuje postavě vyrábět předměty ze dřeva. Umí vyrobit různé drobné předměty ze dřeva jako třeba šípy a dřevěné panenky. Mělo by se jednat především o drobnější dřevěné předměty.  Dále umí vyrábět pasti nutné pro lov zvěře.
Řezbář

Tato větev umožňuje opracovávat dřevo a vytvářet z něj umělecká řezbářská díla.
Stavitel

Tato dovednost je nutnou podmínkou pro stavbu domů. Stavitel do začátku umí postavit pouze jednoduchý dům. Pro složitější stavby musí  hledat stavební plány, které je možné získat na různých místech ve hře.
Sochař

Tato dovednost umožňuje vytvářet sochařská díla.
Projektant obytných staveb

Tato dovednost je podmínkou pro vytváření domů a dalších podobných budov. Pro většinu budov je nutné vytvořit plány.
Projektant infrastruktury

Tato dovednost je podmínkou pro stavbu budov infrastruktury jako cesty, hradby a podobné.
Projektant ostatních budov

Tato dovednost je podmínkou pro stavbu všech ostatních velmi specifických budov jako například chrámů.
Zámečník

Postava s touto schopností se naučí vyrábět jednoduché zámky. Zámek je obecně malá krabička, ke které jsou připevněny spletené drátky. Kolik a jak dlouhých drátků od sebe odlišuje jednotlivé zámky.

Jednoduchý zámek je spletený ze třech vláken o délce 15cm.
Mistr zámečník

Každý mistr umí vyrobit i zámky složitější než základní. Konkrétně se jedná o zámky profilové, šifrové a heslové.
Zámky šifrové

Je spleten z 5ti vláken o délce 20 cm.
Zámky heslové

Je spleten ze 7mi vláken o délce 15 cm.
Zámky profilové

Je spleten ze 4 vláken o délce 30 cm.
