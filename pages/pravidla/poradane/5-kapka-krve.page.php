<h2>Vyhláška</h2>
<p class="bez ui">provincie Polesí, státu Alderah - Lithénské federace</p>
<p>Na vědomost se dává obyvatelstvu, že v obci Kyjov jsou od 6.dne 7.týdne Zimy zpřísněny tresty za porušení intolerančního patentu. Přistižený kacíř má právo vzdát se své víry. Pak je odsouzen k milosrdné smrti stětím. V opačném případě bude kacíř zaživa naražen na kůl a vystaven pro výstrahu. Obyvatelstvu provincie je dále povoleno v rámci intolerančního patentu podnikat veškeré kroky k umožnění zastavení kultu Cesty krve. Pokud bude občan posléze schopen dát důkaz o svém oprávněném jednání, může zasáhnout i se zbraní.
</p><p>Pozn.k vyhlášce: Všechna tato opatření byla vystavena po někdejších zkušenostech ve městě Haszowě, kde naprosto nekontrolovaně vznikla svatyně krvavého Hamera a bylo v ní provedeno několik obětí šíleným fanatikem Karkulem a jeho nohsledy. Karkul posléze nepotrestán naprosto v klidu odešel z města a nyní se jeho útočištěm prý stal Kyjovský les…
<br><br></p><p>
Dále se vyhlašuje, že v oblasti kolem obce Kyjov se zdržuje několik osob lidského pokolení, na jejichž hlavy je vypsána vysoká odměna. Jsou to následující:
</p>
<p><strong class="l blo">Karkul</strong>
Tento muž zahalený tajemstvím je jedním z mála zbylých veleknězů Cesty krve. Má být souzen pro četné vraždy v rámci rituálů víry v Cestu krve, pro podlé intrikaření, klevety a v neposlední řadě pro pokus o vraždu Yenis, toho času radní města Haszowa. POZOR! Tento muž může být nebezpečný. Dokáže prý pomocí nejrůznějších herezí ovládat váš strach a říká se, že umí hovořit s dušemi mrtvých. Odměna za živého či mrtvého činí 3 Zlatník
</p>
<p><strong class="l blo">Eruvio</strong>
Muž, který podobně jako kněží Karkul stojí za haszowskými aférami s mrtvými těly. Poté, co se vlády nad Haszowem ujal jeho bratr Rufio, odešel z města. Údajně se má pod Karkulovým učením stát Bojovníkem Krve. Odměna za živého či mrtvého činí 2 Zlatníky
</p>
<p><strong class="l blo">Prosper řečený Hroch</strong>
Veliký mužský s velikou silou. Je Karkulovým oddaným sluhou a zároveň fanatickým uctívačem krvavé podstaty boha Haemera. Tribunál jej žádá pro stejné prohřešky, jako kněžího Karkula. Odměna za živého či mrtvého činí 1 Zlatník
</p>
<h3>Klepy</h3>
<p>Ve zdejším kraji byl prý krom kacířů spatřen i <strong>Zagro řečený Kudla.</strong></p><p>
O něm se říká, že se stal jedním z nejnebezpečnějším mužem na území Lithénské federace. Možná se objevil zlákán odměnou za hlavy heretiků, možná jen tak z rozmaru, jak už mu to je podobné. POZOR! Tato informace není přímo potvrzena, může se jednat o planý poplach.
</p><p>Dále se mezi lidmi proslýchá, že:
<ul>
<li>z lesa už se nevrátilo několik sedláků</li>
<li>pan Zagro v lese není, zato se nedaleko v hostinci ubytoval nějaký pan Vinchesco</li>
<li>se v lese zdržuje nějaký pomatený stařík, který každému vnucuje své choromyslné vize</li>
<li>kdosy vyhloubil daleko v lese 3 hroby (pro koho?)</li>
<li>v lese straší duch kovářky Caly, a že každý nevěrný manžel by se před ní měl mít na pozoru.</li>
<li>vlci chodí v noci spát do vesnice, protože se v lese bojí</li>
<li>zaplanou hranice</li>
<li>se vyrábí čím dál tím víc pochodní</li>
</ul>