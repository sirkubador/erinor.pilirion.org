<p>Poslední sních na úbočí hor ještě zcela neroztál a skupinky odvážlivců se už objevují za hradbami města Forlorn. Všechny je sem přivedla vidina velkého bohadství, které mohou vydělat na surovinách trpaslíků z města Boronuk. Všechny tyto pošetilé snílky prahnoucí po bohadství čeká ovšem náročný a dlouhý pochod uzounkými a strastiplnými cestami přes pohoří Stumel. Ve Forlornu je jen pár horalů kteří znají cestu a hory tak dobře, aby mohli vést ty, jenž se odvážili do hor.</p>
