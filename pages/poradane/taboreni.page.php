<h1>Závazná pravidla táboření:</h1>
 <ul>
	<li>Na tábořišti budou k dispozici pouze 3 organizátory jasně vytyčená ohniště. Platí naprostý zákaz vytváření dalších. Hasicí přístroj je viditelně vyvěšen na orgovně. 
	</li><li>Louka je v myslivecky cenné lokalitě a proto platí dodržování lesního a nočního klidu. Zpěv u ohně ano, ale v rozumné hlasitosti. 
	</li><li>Kouření včetně vodních dýmek v lese je zakázáno.
	</li><li>Po tábořišti budou rozmístěny improvizované odpadkové koše. Udržujte tedy pořádek a nenechávejte po sobě odpadky. V lese je zakázáno nechávat jakékoli odpadky!
	</li><li>Dřevo na oheň zpracovávejte mimo louku s tábořištěm. 
	</li><li>Při bourání stanů s velkými kolíky (gotyčáky) po sobě díry zasypte.
	</li><li>Zdravotnický stan bude označen „vlaječkou“ s červeným křížem. V případě jakéhokoli závažného úrazu jej křikem „zdravotník“ přivolejte.
	</li><li>Na herním území se vyskytují nebezpečná místa (skály, prudké svahy), buďte opatrní a upozorňujte na případná nebezpečí. V krajní nouzi vykřikněte „rudá stop“. Všichni v doslechu okamžitě přestanou v činnosti a řeší nastalý problém. 
	</li><li>Své psí miláčky prosím nechte doma. 
 </li>

<h2>Další poznámky</h2>
 <ul>	
	<li>Na herním území je pramen s pitnou vodou. Z tohoto důvodu bude zajištěna pouze voda na pití v průběhu hry. 
 </li>
 
