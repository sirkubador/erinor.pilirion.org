<p>Na rozpočet je třeba říct, že organizátorům není proplácen provolaný telefon a nebyla proplacena část benzínu. Cca 1000 listů papíru bylo tisknuto zadarmo.
 Položky jsou shrnuté do jednotlivých sekcí, tak aby vytvářeli jednotné celky. Nebudeme rozepisovat náklady na jednotlivé položky, seznam je opravdu hodně dlouhý. 
Každoročně se snažíme optimalizovat hospodu, letos se to bohužel nepovedlo. Do příštích ročníků chystáme změny hospody, které vyplynou z analýzy jejích tržeb.
 Rádi přijmeme nabídky, které by nám pomohli náklady snížit (tisk, lamino, apod.) leč důležitý je také výkon. Někdy je pro nás snazší danou věc koupit než sehnat a s tím nic neuděláme.
 Již nyní sehnat všechny věci a zabalit je trvá více než pět dní.  
</p>
